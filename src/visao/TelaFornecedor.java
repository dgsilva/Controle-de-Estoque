    private void editarfornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarfornecedorActionPerformed
        try{
            int opcao=JOptionPane.showConfirmDialog(null, "Deseja realizar  alteração no registro?","Atenção",JOptionPane.YES_NO_OPTION);
            if(opcao==JOptionPane.YES_NO_OPTION){
                Fornecedor c=new Fornecedor();
                c.setcodigofornecedor(Integer.valueOf(codigofornecedor.getText()));
                c.setnomefornecedor(String.valueOf(nomefornecedor.getText()));
                c.setcontatofornecedor(String.valueOf(contatofornecedor.getText()));
                Banco banco =new Banco();
                banco.alterarFornecedor(c);
                JOptionPane.showConfirmDialog(null, "Alteração Efetuada com sucesso");
                desabilitaDados();
                editarfornecedor.setEnabled(false);
                excluirfornecedor.setEnabled(true);
                sairfornecedor.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(null, "Alteração não realizada");
                desabilitaDados();         
            }
        } catch(Exception e ){
            e.printStackTrace();
        }
    }//GEN-LAST:event_editarfornecedorActionPerformed

