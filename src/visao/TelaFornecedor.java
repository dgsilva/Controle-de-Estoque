      private void mostrarpesquisa (List <Fornecedor> fornecedores){
while (tmFornecedor.getRowCount() > 0) {
tmFornecedor.removeRow(0);
}
if (fornecedores.size() == 0) {
JOptionPane.showMessageDialog (null, "nenhum Fornecedor Cadastrado");
} else {
String[] linha = new String[] {null, null, null,};
for (int i = 0; i < fornecedores.size(); i++){

tmFornecedor.addRow(linha);
tmFornecedor.setValueAt (fornecedores.get(i).getcodigofornecedor(), i, 0);
tmFornecedor.setValueAt (fornecedores.get(i).getnomefornecedor(), i, 1);
tmFornecedor.setValueAt (fornecedores.get(i).getcontatofornecedor(), i, 2);

}
}
}
      private void tabelafornecedorLinhaSelecionada (JTable tabela){
if (tabelafornecedor.getSelectedRow() != -1){
habilitaDados();
codigofornecedor.setText (String.valueOf (fornecedores.get(tabela.getSelectedRow()).getcodigofornecedor()));
nomefornecedor.setText(fornecedores.get(tabela.getSelectedRow()).getnomefornecedor());
contatofornecedor.setText(fornecedores.get(tabela.getSelectedRow()).getcontatofornecedor());
} else {
LimparDados ();
}
      }    
